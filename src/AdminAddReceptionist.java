    private void ButtonviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonviewActionPerformed
        // if (cmbmaritialstatus.getSelectedIndex() == 0 || cmbgender.getSelectedIndex() == 0 || cmbbloodgroup.getSelectedIndex() == 0 || txtname.getText().trim().isEmpty() || txtfathersname.getText().trim().isEmpty() || txtaddress.getText().trim().isEmpty() || txtcontectno.getText().trim().isEmpty() || txtpassword.getText().trim().isEmpty() || txtemail.getText().trim().isEmpty() || txtsalary.getText().trim().isEmpty()) {
        //     JOptionPane.showMessageDialog(this, "one or more field is empty");
        //     return;
        // }

        if (txtrid.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Plese Enter Receptionist ID");
            return;
        }
        int rid = Integer.parseInt(txtrid.getText());

        try {

            Database db = new Database();
            PreparedStatement stmt = db.con.prepareStatement("select * from RECEPTIONIST where rid = ?");
            stmt.setInt(1, rid);

            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                txtname.setText(rs.getString(2));
                txtfathersname.setText(rs.getString(3));
                txtaddress.setText(rs.getString(4));
                txtsalary.setText(String.valueOf(rs.getInt(5)));
                txtcontectno.setText(rs.getString(6));
                txtemail.setText(rs.getString(7));
                // DateChooserjoining.setDate(rs.getDate(8));
                String joiningDateString = rs.getString(8);
                DateFormat joiningDateFormat = new SimpleDateFormat("EEE MMM dd HH:mm:ss zzz yyyy", Locale.ENGLISH);
                java.util.Date joiningDate = joiningDateFormat.parse(joiningDateString);
                DateChooserjoining.setDate(joiningDate);

                txtpassword.setText(rs.getString(9));
                String dobString = rs.getString(10);
                DateFormat dobFormat = new SimpleDateFormat("EEE MMM dd HH:mm:ss zzz yyyy", Locale.ENGLISH);
                java.util.Date dob = dobFormat.parse(dobString);
                DateChooserdob.setDate(dob);

                cmbbloodgroup.setSelectedItem(rs.getString(11));
                cmbmaritialstatus.setSelectedItem(rs.getString(12));
                cmbgender.setSelectedItem(rs.getString(13));
            } else {
                JOptionPane.showMessageDialog(null, "RECEPTIONIST ID NOT FOUND.");
            }

            rs.close();
            stmt.close();
            db.con.close();
        } catch (SQLException e) {
            e.printStackTrace();
        } catch (ParseException ex) {
            Logger.getLogger(AdminUpdateDoctor.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_ButtonviewActionPerformed

