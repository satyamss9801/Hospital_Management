    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtpassworddoctorlogin.getText().trim().isEmpty() || txtdiddoctorlogin.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "plese enter some data");
            txtpassworddoctorlogin.setText("");
            txtdiddoctorlogin.setText("");
            return;
        }
        int did = Integer.parseInt(txtdiddoctorlogin.getText());
        String password = txtpassworddoctorlogin.getText();

        try {
            Database dbcon = new Database();
            String s = "select * from doctor where did='" + did + "' and password='" + password + "'";

            ResultSet res = dbcon.sm.executeQuery(s);

            if (res.next()) {
                isLoggedIn = true;
                new DoctorDashboard(Integer.parseInt(txtdiddoctorlogin.getText())).setVisible(true);
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(this, "plese enter valid data");
                txtpassworddoctorlogin.setText("");
                txtdiddoctorlogin.setText("");
                this.setVisible(false);
                this.setVisible(true);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton1ActionPerformed

