    private void ButtonupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonupdateActionPerformed
        if (txtname.getText().trim().isEmpty()
                || txtage.getText().trim().isEmpty()
                || txtbloodgroup.getText().trim().isEmpty()
                || txtdid.getText().trim().isEmpty()
                || txtdoctornamw.getText().trim().isEmpty()
                || txtfee.getText().trim().isEmpty()
                || cmbdepartment.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "One or more fields are empty.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            int apid = Integer.parseInt(txtapnid.getText()); // Appointment ID
            int pid = Integer.parseInt(txtpid.getText()); // Patient ID
            String pname = txtname.getText();
            int age = Integer.parseInt(txtage.getText());
            String bloodgroup = txtbloodgroup.getText();
            String department = cmbdepartment.getSelectedItem().toString();
            int did = Integer.parseInt(txtdid.getText()); // Doctor ID
            String doctorname = txtdoctornamw.getText();
            int consultantfee = Integer.parseInt(txtfee.getText());

            // Get the selected date and format it as "dd/MM/yyyy"
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            String date = dateFormat.format(DateChooserdate.getDate());

            Database db = new Database();
            String query = "UPDATE appointment SET pid=?, pname=?, age=?, bloodgroup=?, department=?, did=?, doctorname=?, consultantfee=?, date=? WHERE apid=?";
            PreparedStatement pst = db.con.prepareStatement(query);
            pst.setInt(1, pid);
            pst.setString(2, pname);
            pst.setInt(3, age);
            pst.setString(4, bloodgroup);
            pst.setString(5, department);
            pst.setInt(6, did);
            pst.setString(7, doctorname);
            pst.setInt(8, consultantfee);
            pst.setString(9, date);
            pst.setInt(10, apid);

            int rowsAffected = pst.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Appointment updated successfully.");
                clear();

                // Clear text fields
            } else {
                JOptionPane.showMessageDialog(this, "Failed to update appointment.");
            }

            pst.close();
            db.con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_ButtonupdateActionPerformed

