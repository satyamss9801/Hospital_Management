    private void ButtondeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtondeleteActionPerformed
        if (txtapnid.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter Appointment ID ");
            return;
        }
        try {
            int apid = Integer.parseInt(txtapnid.getText()); // Get the appointment ID

            Database db = new Database();
            String query = "DELETE FROM appointment WHERE apid = ?";
            PreparedStatement pst = db.con.prepareStatement(query);
            pst.setInt(1, apid);

            int rowsAffected = pst.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Record deleted successfully.");
                clear();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete record.");
            }

            pst.close();
            db.con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_ButtondeleteActionPerformed

