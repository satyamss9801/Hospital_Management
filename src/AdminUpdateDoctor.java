    private void ButtonviewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonviewActionPerformed

        if (txtdid.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ENTER DOCTOR'S ID");
            return;
        }
        int did = Integer.parseInt(txtdid.getText());

        try {
            Database db = new Database();
            PreparedStatement stmt = db.con.prepareStatement("SELECT * FROM doctor WHERE did = ?");
            stmt.setInt(1, did);
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                txtname.setText(rs.getString(2));
                txtfathersname.setText(rs.getString(3));
                cmbbloodgroup.setSelectedItem(rs.getString(4));
                txtaddress.setText(rs.getString(5));
                txtcontectno.setText(rs.getString(6));
                cmbdepartment.setSelectedItem(rs.getString(7));
                txtemail.setText(rs.getString(8));
                txtschedule.setText(rs.getString(9));
                txtpassword.setText(rs.getString(10));

                String dobString = rs.getString(11);
                DateFormat dobFormat = new SimpleDateFormat("EEE MMM dd HH:mm:ss zzz yyyy", Locale.ENGLISH);
                java.util.Date dob = dobFormat.parse(dobString);
                DateChooserdob.setDate(dob);

                cmbgender.setSelectedItem(rs.getString(12));
                cmbmaritialstatus.setSelectedItem(rs.getString(13));

                String joiningDateString = rs.getString(14);
                DateFormat joiningDateFormat = new SimpleDateFormat("EEE MMM dd HH:mm:ss zzz yyyy", Locale.ENGLISH);
                java.util.Date joiningDate = joiningDateFormat.parse(joiningDateString);
                DateChooserjoining.setDate(joiningDate);

                txtexperience.setText(rs.getString(15));
                txtfee.setText(String.valueOf(rs.getInt(16)));

            } else {
                JOptionPane.showMessageDialog(null, "Doctor ID not found.");
            }

            rs.close();
            stmt.close();
            db.con.close();
        } catch (SQLException e) {
            e.printStackTrace();
        } catch (ParseException ex) {
            Logger.getLogger(AdminUpdateDoctor.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_ButtonviewActionPerformed

