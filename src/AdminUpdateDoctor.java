    private void ButtonupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonupdateActionPerformed
        if (txtfee.getText().trim().isEmpty() || cmbmaritialstatus.getSelectedIndex() == 0 || cmbgender.getSelectedIndex() == 0 || cmbbloodgroup.getSelectedIndex() == 0 || txtname.getText().trim().isEmpty() || txtfathersname.getText().trim().isEmpty() || txtaddress.getText().trim().isEmpty() || txtcontectno.getText().trim().isEmpty() || cmbdepartment.getSelectedIndex() == 0 || txtemail.getText().trim().isEmpty() || txtschedule.getText().trim().isEmpty() || txtpassword.getText().trim().isEmpty() || txtexperience.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "One or more fields are empty");
            return;
        }
        try {
            int did = Integer.parseInt(txtdid.getText());
            String dname = txtname.getText();
            String fathersname = txtfathersname.getText();
            String bloodgroup = cmbbloodgroup.getSelectedItem().toString();
            String address = txtaddress.getText();
            String contectno = txtcontectno.getText();
            String department = cmbdepartment.getSelectedItem().toString();
            String emailid = txtemail.getText();
            String schedule = txtschedule.getText();
            String password = txtpassword.getText();
            String dob = DateChooserdob.getDate().toString();
            String gender = cmbgender.getSelectedItem().toString();
            String maritialstatus = cmbmaritialstatus.getSelectedItem().toString();
            String joningdate = DateChooserjoining.getDate().toString();
            String experience = txtexperience.getText();
            int fee = Integer.parseInt(txtfee.getText());

            try {
                Database db = new Database();
                String query = "UPDATE doctor SET dname=?, fathersname=?, bloodgroup=?, address=?, contectno=?, department=?, emailid=?, schedule=?, password=?, dob=?, gender=?, maritialstatus=?, joiningdate=?, experience=?,fee=? WHERE did=?";
                PreparedStatement pst = db.con.prepareStatement(query);
                pst.setString(1, dname);
                pst.setString(2, fathersname);
                pst.setString(3, bloodgroup);
                pst.setString(4, address);
                pst.setString(5, contectno);
                pst.setString(6, department);
                pst.setString(7, emailid);
                pst.setString(8, schedule);
                pst.setString(9, password);
                pst.setString(10, dob);
                pst.setString(11, gender);
                pst.setString(12, maritialstatus);
                pst.setString(13, joningdate);
                pst.setString(14, experience);
                pst.setInt(15, fee);
                pst.setInt(16, did);

                int affectedRows = pst.executeUpdate();
                if (affectedRows == 1) {
                    JOptionPane.showMessageDialog(this, "Update successful");
                    clear();
                } else {
                    JOptionPane.showMessageDialog(null, "Something went wrong");
                }
            } catch (Exception e) {
                e.printStackTrace();

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_ButtonupdateActionPerformed

    private void ButtondeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtondeleteActionPerformed
        if (txtdid.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ENTER DOCTOR'S ID");
            return;
        }
        int did = Integer.parseInt(txtdid.getText());
        try {
            Database db = new Database();
            String query = "DELETE FROM doctor WHERE did = ?";
            PreparedStatement pst = db.con.prepareStatement(query);
            pst.setInt(1, did);

            int affectedRows = pst.executeUpdate();
            if (affectedRows == 1) {
                JOptionPane.showMessageDialog(this, "Delete successful");
                clear();
            } else {
                JOptionPane.showMessageDialog(null, "Record not found");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_ButtondeleteActionPerformed

