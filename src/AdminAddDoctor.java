    private void ButtonaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonaddActionPerformed
        if (txtfee.getText().trim().isEmpty() || cmbmaritialstatus.getSelectedIndex() == 0 || cmbgender.getSelectedIndex() == 0 || cmbbloodgroup.getSelectedIndex() == 0 || txtname.getText().trim().isEmpty() || txtfathersname.getText().trim().isEmpty() || txtaddress.getText().trim().isEmpty() || txtcontectno.getText().trim().isEmpty() || cmbdepartment.getSelectedIndex() == 0 || txtemail.getText().trim().isEmpty() || txtschedule.getText().trim().isEmpty() || txtcontectno.getText().trim().isEmpty() || txtpassword.getText().trim().isEmpty() || txtexperience.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "one or more field is empty");
            return;
        }
        String dname = txtname.getText();
        String fathersname = txtfathersname.getText();
        String bloodgroup = cmbbloodgroup.getSelectedItem().toString();
        String address = txtaddress.getText();
        String contectno = txtcontectno.getText();
        String department = cmbdepartment.getSelectedItem().toString();
        String emailid = txtemail.getText();
        String schedule = txtschedule.getText();
        String password = txtpassword.getText();
        //java.util.Date utilPackageDate = DateChooserdob.getDate();
        String dob = DateChooserdob.getDate().toString();
        String gender = cmbgender.getSelectedItem().toString();
        String maritialstatus = cmbmaritialstatus.getSelectedItem().toString();

        String joiningdate = DateChooserjoining.getDate().toString();
        String experience = txtexperience.getText();
        int fee = Integer.parseInt(txtfee.getText());
        try {

            Database db = new Database();

            String rg = ("insert into doctor(dname,fathersname,bloodgroup,address,contectno,department,emailid,schedule,password,dob,gender,maritialstatus,joiningdate,experience,fee)values(?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)");
            PreparedStatement pst = db.con.prepareStatement(rg);
            // pst.setDate(2, new Date(utilPackageDate.getTime()));
            pst.setString(1, dname);
            pst.setString(2, fathersname);
            pst.setString(3, bloodgroup);
            pst.setString(4, address);
            pst.setString(5, contectno);
            pst.setString(6, department);
            pst.setString(7, emailid);
            pst.setString(8, schedule);
            pst.setString(9, password);
            pst.setString(10, dob);
            pst.setString(11, gender);
            pst.setString(12, maritialstatus);
            pst.setString(13, joiningdate);
            pst.setString(14, experience);
            pst.setInt(15, fee);
            int aa = pst.executeUpdate();
            if (aa == 1) {
                JOptionPane.showMessageDialog(this, "Add Success");
                clear();
            } else {
                JOptionPane.showMessageDialog(null, "Someting went wrong");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_ButtonaddActionPerformed

